# Implementação simples do print, imprimindo uma única string
print("Bem vindo ao Wildson Lab!")

# Concatenando diversas strings (separador é o espaço por padrão)
print("Bem", "vindo", "ao", "Wildson", "Lab!")

# Modificando o separador
print("Bem", "vindo", "ao", "Wildson", "Lab!", sep="_")

# Removendo a quebra de linha no final do print
print("Bem", "vindo", "ao", "Wildson", "Lab!", end="")
print(" Este texto será impresso na mesma linha que o anterior.")
