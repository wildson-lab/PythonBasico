# 02 - Variaveis e Tipos
# Este codigo apresenta conceitos basicos sobre variaveis, e mostra como obter
# e exibir o tipo de cada variavel.

# Definicao das variaveis
nome = "Wildson"
idade = 26

# Imprime cada variavel e o seu tipo.
print("Nome: ")
print(nome)
print("Tipo da variável 'nome': ")
print(type(nome))

print("\n")

print("Idade: ")
print(str(idade))
print("Tipo da variável 'idade':")
print(type(idade))
