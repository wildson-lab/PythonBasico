# 03 - Operacoes Matematicas
# Este codigo apresenta operacoes matematicas basicas realizadas em Python.

# Definicao de variaveis
a = 5
b = 3

# Variaveis que armazenam as operacoes realizadas
soma = a+b
subtracao = a-b
multiplicacao = a*b
divisao = a/b
divInteira = a//b
resto = a%b
expoente = a**b

# Imprimindo os resultados
print("Soma: {}".format(soma))
print("Subtracao: {}".format(subtracao))
print("Multiplicacao: {}".format(multiplicacao))
print("Divisao: {}".format(divisao))
print("Divisao inteira: {}".format(divInteira))
print("Resto: {}".format(resto))
print("Expoente: {}".format(expoente))


