# Este codigo apresenta operacoes matematicas basicas realizadas em Python.

# Definicao de variaveis
a = 5
b = 3

# Operações matemáticas
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
divInteira = a // b
resto = a % b
expoente = a ** b

# Imprimindo os resultados
print(f"a = {a}")
print(f"b = {b}")
print(f"Soma: {soma}")
print(f"Subtracao: {subtracao}")
print(f"Multiplicacao: {multiplicacao}")
print(f"Divisao: {divisao}")
print(f"Divisao inteira: {divInteira}")
print(f"Resto: {resto}")
print(f"Expoente: {expoente}")
